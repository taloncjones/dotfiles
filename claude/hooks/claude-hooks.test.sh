#!/bin/sh
set -e

PASS=0
FAIL=0
ATTRIBUTION_SUBJECT="A""I"

run_hook() {
    hook="$1"
    payload="$2"
    printf '%s\n' "$payload" | "$hook" >/tmp/claude-hook-test.out 2>/tmp/claude-hook-test.err
}

assert_blocks() {
    label="$1"
    hook="$2"
    payload="$3"
    if run_hook "$hook" "$payload"; then
        printf 'FAIL  %s\n' "$label" >&2
        FAIL=$((FAIL + 1))
    else
        printf 'PASS  %s\n' "$label"
        PASS=$((PASS + 1))
    fi
}

assert_allows() {
    label="$1"
    hook="$2"
    payload="$3"
    if run_hook "$hook" "$payload"; then
        printf 'PASS  %s\n' "$label"
        PASS=$((PASS + 1))
    else
        printf 'FAIL  %s\n' "$label" >&2
        FAIL=$((FAIL + 1))
    fi
}

assert_blocks "blocks attribution in code content" \
    claude/hooks/no_ai_comments.py \
    "{\"tool_name\":\"Write\",\"tool_input\":{\"file_path\":\"src/app.py\",\"content\":\"# Generated by ${ATTRIBUTION_SUBJECT}\\n\"}}"

assert_allows "allows attribution fixtures in workflow hooks" \
    claude/hooks/no_ai_comments.py \
    "{\"tool_name\":\"Write\",\"tool_input\":{\"file_path\":\"claude/hooks/example.py\",\"content\":\"# Generated by ${ATTRIBUTION_SUBJECT}\\n\"}}"

assert_blocks "blocks emoji in code content" \
    claude/hooks/emoji_guard.py \
    "$(printf '%b' '{"tool_name":"Write","tool_input":{"file_path":"src/app.py","content":"print(\"hi \360\237\230\200\")"}}')"

assert_allows "allows emoji fixtures in workflow rules" \
    claude/hooks/emoji_guard.py \
    "$(printf '%b' '{"tool_name":"Write","tool_input":{"file_path":"codex/rules/example.rules","content":"fixture \360\237\230\200"}}')"

assert_blocks "blocks sensitive env file path" \
    claude/hooks/block_secrets.py \
    '{"tool_name":"Read","tool_input":{"file_path":".env.local"}}'

assert_blocks "blocks secrets path segment" \
    claude/hooks/block_secrets.py \
    '{"tool_name":"Read","tool_input":{"file_path":"config/secrets/prod.json"}}'

assert_allows "allows env example path" \
    claude/hooks/block_secrets.py \
    '{"tool_name":"Read","tool_input":{"file_path":".env.example"}}'

assert_allows "allows public key path" \
    claude/hooks/block_secrets.py \
    '{"tool_name":"Read","tool_input":{"file_path":"ssh/keys/id_ed25519_personal.pub"}}'

assert_allows "allows benign secret word in source name" \
    claude/hooks/block_secrets.py \
    '{"tool_name":"Read","tool_input":{"file_path":"src/secrets_manager/util.py"}}'

assert_allows "allows workflow hook secret checker path" \
    claude/hooks/block_secrets.py \
    '{"tool_name":"Read","tool_input":{"file_path":"claude/hooks/block_secrets.py"}}'

# Settings drift: hand-merged machines that missed a SessionStart hook
# entry must fail visibly instead of silently lacking the account-mismatch
# guard. Both account config dirs carry their own machine-local settings.json
# (~/.claude personal, ~/.claude-work work); check every one that exists.
# Skipped when no live settings file exists (fresh machine or CI).
for settings_dir in "$HOME/.claude" "$HOME/.claude-work"; do
    if [ -f "$settings_dir/settings.json" ]; then
        for required_hook in account_guard.py; do
            if SETTINGS_PATH="$settings_dir/settings.json" REQUIRED_HOOK="$required_hook" python3 - <<'PY'
import json
import os
import sys

cfg = json.load(open(os.environ["SETTINGS_PATH"]))
required = os.environ["REQUIRED_HOOK"]
entries = cfg.get("hooks", {}).get("SessionStart", [])
cmds = [h.get("command", "") for e in entries for h in e.get("hooks", [])]
sys.exit(0 if any(c.endswith("hooks/" + required) for c in cmds) else 1)
PY
            then
                printf 'PASS  settings: %s SessionStart hooks register %s\n' "$settings_dir" "$required_hook"
                PASS=$((PASS + 1))
            else
                printf 'FAIL  settings: %s SessionStart hooks missing %s (hand-merge required)\n' "$settings_dir" "$required_hook" >&2
                FAIL=$((FAIL + 1))
            fi
        done
    else
        printf 'SKIP  settings: no live %s/settings.json\n' "$settings_dir"
    fi
done

printf '\n%d passed, %d failed\n' "$PASS" "$FAIL"
[ "$FAIL" = 0 ]
